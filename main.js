let currentLang = localStorage.getItem('daoyu_lang') || 'zh';

function applyLang(lang) {
    currentLang = lang;
    localStorage.setItem('daoyu_lang', lang);
    const dict = T[lang];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (dict[key] !== undefined) el.textContent = dict[key];
    });
    document.documentElement.lang = lang === 'zh' ? 'zh-Hans' : lang === 'tw' ? 'zh-Hant' : 'en';
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    // Update page title
    document.title = lang === 'zh'
        ? '道玉留学 — 全球英语授课升学咨询'
        : lang === 'tw'
        ? '道玉留學 — 全球英語授課升學諮詢'
        : 'DaoYu Consulting — Global English-Taught Programs';
}

document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

// Apply saved language on load
if (currentLang !== 'zh') applyLang(currentLang);


// ===== LOADER =====
window.addEventListener('load', () => {
    const bar  = document.getElementById('loaderBar');
    const hero = document.getElementById('hero');
    requestAnimationFrame(() => { bar.style.width = '100%'; });
    setTimeout(() => {
        document.getElementById('loader').classList.add('done');
        hero.classList.add('hero-animate');
    }, 620);
});


// ===== NAVIGATION =====
const nav    = document.getElementById('nav');
const heroEl = document.getElementById('hero');

const heroObserver = new IntersectionObserver(entries => {
    entries.forEach(e => nav.classList.toggle('visible', !e.isIntersecting));
}, { threshold: 0.1 });
heroObserver.observe(heroEl);

window.addEventListener('scroll', () => {
    nav.classList.toggle('scrolled', window.scrollY > 40);
}, { passive: true });


// ===== SECTION FADE-IN =====
const fadeObserver = new IntersectionObserver(entries => {
    entries.forEach(e => {
        if (e.isIntersecting) {
            e.target.classList.add('in-view');
            fadeObserver.unobserve(e.target);
        }
    });
}, { threshold: 0.08, rootMargin: '0px 0px -48px 0px' });

document.querySelectorAll('.fade-section').forEach(el => fadeObserver.observe(el));


// ===== SMOOTH SCROLL =====
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', e => {
        const target = document.querySelector(anchor.getAttribute('href'));
        if (target) {
            e.preventDefault();
            const top = target.getBoundingClientRect().top + window.scrollY - 68;
            window.scrollTo({ top, behavior: 'smooth' });
        }
    });
});
